# 数字类型运算符

a = int(input("Please give me a integer: "))
b = int(input("Please give me a integer again: "))

# 加法         (+)
print(a+b)
# 减法         (-)
print(a-b)
# 乘法         (*)
print(a*b)
# 真除法       (/)
print(a/b)
# 整除         (//)
print(a//b)     
# 取余         (%)
print(a%b)
# 负号         (-)
print(-a,-b)
# 正号         (+)
print(+a,+b)
# 幂运算       (**)
print(a**b)

# Python 中通常满足:
# a == (a // b) * b + (a % b)
# 负数整除 例如 -7 // 2
# 普通除法 -7 / 2 = -3.5
# 整除是向负无穷方向取整,所以 -7 // 2 == -4

# 负数取余 例如 -7 % 3
# 先计算整除 -7 // 3 == -3
# 再利用公式 -7 == -3 * 3 + 2
# 所以 -7 % 3 == 2

# 取余计算公式 a % b == a - (a // b) * b


# 以上所有运算符都可以与赋值运算符(=)组合，构成自修改运算符
# += -= *= /= //= %= **=


# 比较运算符
