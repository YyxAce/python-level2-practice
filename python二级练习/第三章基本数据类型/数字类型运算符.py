# 逻辑运算符
# and or not
# 布尔"与" 布尔"或" 布尔"非"
print("逻辑运算符")
# and 找假值
# 返回第一个假值
# 全为真则返回最后一个值

print(3 and 6)
print(0 and 6)
# or 找真值
# 返回第一个真值
# 全为假则返回最后一个值

print(3 or 6)
print(0 or 6)
# not 真假取反，一定返回 True 或 False

